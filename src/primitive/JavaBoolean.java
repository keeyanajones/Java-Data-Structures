/**
 * Java boolean example
 * shows how to declare and use primitive boolean variable inside a java class.
 * 
 * @author Keeyana Jones <keeyanajones@yahoo.com>
 * 
 **/
package primitive;

public class JavaBoolean {
  public static void main(String[] args) {
    /**
     * boolean is simple data type which can have only of two values;
     * true or false.
     **/
    boolean result = true;
    boolean b1 = true;
    boolean b2 = false;
    boolean b3 = (10 > 2);   

    System.out.println("Value of boolean variable result is : " + result);
    System.out.println("Value of boolean variable b1 is : " + b1);
    System.out.println("Value of boolean variable b2 is : " + b2);
    System.out.println("Value of boolean variable b3 is : " + b3);            
  }
}
