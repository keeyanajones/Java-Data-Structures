/**
 * Java float example
 * shows how to declare and use primitive float variable inside a java class.
 * 
 * @author Keeyana Jones <keeyanajones@yahoo.com>
 * 
 **/
package primitive;

public class JavaFloating {
  public static void main(String[] args) {
    /**
     * float is 32 bit single precision type and used when fractional 
     * precision calculation is required.
     **/
    float f = 10.4f;
    float f1  = 123.4f;
    float pi =  3.14_15F;    
    
    System.out.println("Value of float variable f is : " + f);                
    System.out.println("Value of float variable f1 is : " + f1);                
    System.out.println("Value of float variable pi is : " + pi);                
  }
}
